#include <cstdio>
#include <cmath>

struct titik {
	int x,y,z;
};

titik a, b, c;
int main ()
{
	scanf("%d", &a.x);
	scanf("%d", &a.y);
	scanf("%d", &a.z);
	scanf("%d", &b.x);
	scanf("%d", &b.y);
	scanf("%d", &b.z);
	scanf("%d", &c.x);
	scanf("%d", &c.y);
	scanf("%d", &c.z);
	
	printf("%d %d %d\n", a.x, b.x, c.x);
	printf("%d %d %d\n", a.y, b.y, c.y);
	printf("%d %d %d\n", a.z, b.z, c.z);
}
